def _initials(name: str) -> str:
	parts = [part for part in name.strip().split() if part]
	if not parts:
		return "?"
	if len(parts) == 1:
		return parts[0][0].upper()
	return f"{parts[0][0]}{parts[1][0]}".upper()


def _participant_initials(authors: list[str]) -> str:
	seen: list[str] = []
	for author in authors:
		initial = _initials(author)
		if initial not in seen:
			seen.append(initial)
	return ",".join(seen[:4])


def _clear_demo(db) -> None:
	topic_ids = db.scalars(
		select(models.CommunityTopic.id).where(
			models.CommunityTopic.external_id.like(f"{DEMO_PREFIX}%")
		)
	).all()
	if not topic_ids:
		return
	db.execute(delete(models.CommunityTopicRead).where(models.CommunityTopicRead.topic_id.in_(topic_ids)))
	db.execute(delete(models.CommunityMessage).where(models.CommunityMessage.topic_id.in_(topic_ids)))
	db.execute(delete(models.CommunityTopic).where(models.CommunityTopic.id.in_(topic_ids)))
	db.commit()


def _resolve_master_id(db, author_name: str) -> int | None:
	if author_name != "Елена":
		return None
	master = db.scalar(select(models.Master).where(models.Master.first_name == "Елена").limit(1))
	return master.id if master else None


def seed_community(db, *, clear: bool = False) -> dict[str, int]:
	if clear:
		_clear_demo(db)

	created_topics = 0
	created_messages = 0

	for index, (title, emoji, pinned, messages) in enumerate(TOPICS, start=1):
		external_id = f"{DEMO_PREFIX}{index:02d}"
		existing = db.scalar(
			select(models.CommunityTopic).where(models.CommunityTopic.external_id == external_id)
		)
		if existing:
			continue

		authors = [row[0] for row in messages]
		first_author = authors[0]
		first_text = messages[0][2]
		first_time = BASE_TIME + timedelta(hours=messages[0][1])
		last_row = messages[-1]
		last_time = BASE_TIME + timedelta(hours=last_row[1])

		topic = models.CommunityTopic(
			external_id=external_id,
			title=title,
			author_name=first_author,
			author_master_id=_resolve_master_id(db, first_author),
			emoji=emoji,
			is_pinned=pinned,
			is_closed=False,
			participant_count=len(set(authors)),
			participant_initials=_participant_initials(authors),
			last_message=last_row[2],
			last_message_at=last_time,
			unread_count=0,
		)
		db.add(topic)
		db.flush()

		for msg_index, (author, hours_offset, text) in enumerate(messages, start=1):
			sent_at = BASE_TIME + timedelta(hours=hours_offset)
			master_id = _resolve_master_id(db, author)
			db.add(
				models.CommunityMessage(
					external_id=f"{external_id}-m-{msg_index:02d}",
					topic_id=topic.id,
					author_name=author,
					author_master_id=master_id,
					text=text,
					sent_at=sent_at,
					is_mine=False,
				)
			)
			created_messages += 1

		created_topics += 1

	db.commit()
	return {"topics": created_topics, "messages": created_messages}


def main() -> None:
	parser = argparse.ArgumentParser(description="Seed community topics and discussions")
	parser.add_argument("--clear", action="store_true", help="Remove previous demo topics first")
	args = parser.parse_args()

	with SessionLocal() as db:
		result = seed_community(db, clear=args.clear)
		print(f"OK: topics={result['topics']}, messages={result['messages']}")


if __name__ == "__main__":
	main()
